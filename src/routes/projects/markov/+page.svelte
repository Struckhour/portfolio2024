<div class="max-w-4xl mx-auto">
    <p>A hermit thrush (seen above) is a forest-dwelling bird known for its beautiful, haunting song. However, What most people hear and think of as a singular repeating
    2-second song is actually slightly different songs that it is ordering (like words being ordered into sentences).
    It cycles through these songs and chooses the next one based on what it has previously sung.
    For example, it almost never sings the same song twice in a row.
    </p>
    <p>The question I set out to answer was quite simple---even if complicated in the solving:
    </p>
    <br>
    <div class="text-center"><b>How many previous songs influence the upcoming song choice of a hermit thrush?</b>
    </div>
    <br>
    In other words, if I know the most recent song, does that help me predict what the next song will be? What if I know the most recent two songs? Three songs, etc.?
    <br>
    
